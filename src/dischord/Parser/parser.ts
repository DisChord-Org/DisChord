import { KeyWords } from '../../chord/keywords';
import { Token } from '../../chord/types';
import { DisChordASTNode, DisChordNode, DisChordNodeType } from '../types';

import { Parser } from '../../chord/parser';
import { SubParserClass } from './subparser';

import CommandParser from './Commands/CommandParser';
import MessageParser from './Messages/MessageParser';
import CollectorParser from './CollectorParser';
import ClientParser from './Client/ClientParser';
import EventParser from './Events/EventParser';
import { DisChordError, ErrorLevel } from '../../ChordError';

/**
 * Main Orchestrator for DisChord's syntactic analysis.
 * * This class extends the base Parser and delegates the responsibility of parsing 
 * specific structures (Messages, Commands, Events, etc.) to specialized sub-parsers 
 * using a composition-by-delegation pattern.
 */
export class DisChordParser extends Parser<DisChordNodeType, DisChordNode> {
    /**
     * The inventory of specialists.
     * Adding a class here will register it into the all system.
     */
    private static readonly SubParsers: SubParserClass[] = [
        ClientParser,
        EventParser,
        CommandParser,
        MessageParser,
        CollectorParser
    ];

    /**
     * Initializes a new instance of the DisChordParser.
     * @param tokens - The array of tokens generated by the Lexer.
     * @param current - The starting token index (defaults to 0).
     */
    constructor (tokens: Token[], current: number = 0) {
        super(tokens, current);
    }

    /**
     * Injects DisChord-specific keywords into the global system 
     * so the Lexer can correctly identify them as tokens.
     */
    public static injectStatements() {
        KeyWords.addStatements([ "crear" ]);

        this.SubParsers.forEach(SubParser => {
            SubParser.injectStatements();
        });
    }

    /**
     * Entry point for custom DisChord statements.
     * Analyzes top-level tokens such as 'encender', 'evento', 'crear', etc.
     * @override
     * @returns The generated ASTNode or null if no match is found.
     */
    override parseCustomStatement(): DisChordASTNode | null {
        const token = this.peek();

        const ParserClass = DisChordParser.SubParsers.find(SubParser =>
            SubParser.triggerToken.toUpperCase() === token.type
        );

        if (ParserClass) return new ParserClass(this).parse();

        return null;
    }

    /**
     * Extends the base language's primary expressions.
     * Allows the 'crear' (create) keyword to be treated as a primary expression.
     * Allows 'L_BRACE' to create ODB's.
     * @override
     */
    override parsePrimary(): DisChordASTNode {
        const token = this.peek();

        switch (token.type) {
            case 'CREAR':
                this.consume('CREAR');

                const customStatement = this.parseCustomStatement();
                
                if (!customStatement) throw new DisChordError(
                    ErrorLevel.Parser,
                    `Se esperaba una estructura válida después de 'crear'`,
                    token.location
                ).format();

                return customStatement;
            case 'L_BRACE':
                return this.parseODB();
            default:
                return super.parsePrimary();
        }
    }
}